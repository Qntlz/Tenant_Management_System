            <h5 id="q1">1. What is the difference between Deposit and Security Deposit?</h5>
            <div class="container px-4 pt-3" style="text-align: justify;">
                <p>A rent deposit and a security deposit are both related to renting a place, but they serve slightly different purposes. A rent deposit is usually paid upfront when you sign the lease agreement. It's like a down payment for your first month's rent. This deposit is used to secure the rental property and ensure that you're committed to renting it. It's equal to one month's rent. For example, if your monthly rent is $1,000, you would pay a $1,000 rent deposit.</p>
                <p>On the other hand, a security deposit is a separate amount that the landlord holds onto during your tenancy. Its purpose is to protect the landlord in case there are any damages to the property or if you fail to pay rent. Let's say you accidentally break a window or there's some damage beyond normal wear and tear. The landlord can deduct the cost of repairs from your security deposit.</p>
                <p>So, to summarize, the rent deposit goes towards your first month's rent, while the security deposit acts as a form of protection for the landlord. </p>
            </div>
